function loop_while() {
    console.log("Sequência de números de 0 a 9 usando while:");
    let i = 0;
    while (i <= 9) {
        console.log(i);
        i++;
    }
}

function loop_for() {
    console.log("Sequência de números de 0 a 9 usando for:");
    for (let i = 0; i <= 9; i++) {
        console.log(i);
    }
}